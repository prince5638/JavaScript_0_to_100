// booleans & comparision operator

// boolean
let num1 = 7;
let num2 = "7";

// == vs === 
// The == operator checks for equality after performing type coercion, which means it tries to convert the operands to the same type before making the comparison.
console.log(num1 == num2);
console.log(num1 === num2);

console.log(num1 <= num2);

// != vs !==
console.log(num1 != num2);
console.log(num1 !== num2);